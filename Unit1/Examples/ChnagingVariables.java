public class ChangingVariables {
    public static void main(String[] args) {
        // Declare an integer variable named 'number' and assign it an initial value of 10
        int number = 10;

        // Print the initial value of the variable
        System.out.println("Initial value of number: " + number);

        // Use the assignment operator to change the value of the variable
        number = 20;

        // Print the new value of the variable
        System.out.println("New value of number: " + number);

        // Declare a string variable named 'text' and assign it an initial value
        String text = "Hello";

        // Print the initial value of the variable
        System.out.println("Initial value of text: " + text);

        // Use the assignment operator to change the value of the variable
        text = "Goodbye";

        // Print the new value of the variable
        System.out.println("New value of text: " + text);
    }
}
