public class PrintingInJava {
    public static void main(String[] args) {
        // This is a comment, it is ignored by the compiler

        // Printing a simple message to the console
        System.out.println("Hello, World!");

        // Printing a number
        System.out.println(42);

        // Printing the result of a mathematical operation
        System.out.println(7 + 5);

        // Printing a message with special characters
        System.out.println("Hello, \"Java\"!");

        // Printing multiple  lines
        System.out.println("This is line one.\nThis is line two.");
    }
}